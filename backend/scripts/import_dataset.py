#!/usr/bin/env python3
"""Download a Flickr30k subset from Hugging Face, convert images to WebP, and regenerate ground_truth.jsonc.

Every run wipes backend/data/images/ and backend/data/ground_truth.jsonc so teammates always benchmark on
the same tiny, reproducible slice of the dataset.

Usage (from backend/):
    python3 scripts/import_dataset.py
"""
from __future__ import annotations

import json
import shutil
import zipfile
from pathlib import Path

import pandas as pd
import yaml
from huggingface_hub import hf_hub_download
from PIL import Image

BACKEND_ROOT = Path(__file__).resolve().parent.parent
IMAGES_DIR = BACKEND_ROOT / "data" / "images"
GROUND_TRUTH_PATH = BACKEND_ROOT / "data" / "ground_truth.jsonc"
DATASET_REPO = "nlphuji/flickr30k"
ANNOTATIONS_FILE = "flickr_annotations_30k.csv"
IMAGES_ARCHIVE_FILE = "flickr30k-images.zip"
ARCHIVE_IMAGE_ROOT = "flickr30k-images"
SHUFFLE_SEED = 42

_dataset_cfg = yaml.safe_load((BACKEND_ROOT / "config" / "dataset.yaml").read_text())
NUM_IMAGES: int = _dataset_cfg["num_images"]


def _load_annotations() -> pd.DataFrame:
    annotations_path = hf_hub_download(
        repo_id=DATASET_REPO,
        filename=ANNOTATIONS_FILE,
        repo_type="dataset",
    )
    df = pd.read_csv(annotations_path)
    if "raw" in df.columns:
        df["raw"] = df["raw"].apply(json.loads)
    return df


def _download_archive() -> str:
    return hf_hub_download(
        repo_id=DATASET_REPO,
        filename=IMAGES_ARCHIVE_FILE,
        repo_type="dataset",
    )


def main() -> None:
    df = _load_annotations()
    if df.empty:
        raise SystemExit("Flickr30k annotations are empty; nothing to import.")

    take = min(NUM_IMAGES, len(df))
    subset = df.sample(n=take, random_state=SHUFFLE_SEED).reset_index(drop=True)
    archive_path = _download_archive()

    if IMAGES_DIR.exists():
        shutil.rmtree(IMAGES_DIR)
    IMAGES_DIR.mkdir(parents=True)

    ground_truth: list[dict] = []

    with zipfile.ZipFile(archive_path) as archive:
        for row in subset.itertuples(index=False):
            filename = getattr(row, "filename")
            if not filename:
                continue
            image_id = Path(filename).stem
            image_member = f"{ARCHIVE_IMAGE_ROOT}/{filename}"
            try:
                with archive.open(image_member) as handle:
                    with Image.open(handle) as image:
                        image = image.convert("RGB")
                        image.save(IMAGES_DIR / f"{image_id}.webp", format="WEBP", quality=85)
            except KeyError as exc:
                raise SystemExit(f"Image {image_member} missing from archive.") from exc

            captions = getattr(row, "raw", []) or []
            caption_text = captions[0] if captions else f"Photo {image_id}"
            ground_truth.append({
                "id": f"query_{image_id}",
                "text": caption_text,
            })

    header = (
        "// ground_truth.jsonc — generated by import_dataset.py, do not edit manually.\n"
        "// Each query targets the image whose filename matches the id with 'query_' stripped.\n"
    )
    body = json.dumps(ground_truth, indent=2, ensure_ascii=False)
    GROUND_TRUTH_PATH.write_text(header + body + "\n", encoding="utf-8")

    print(f"Saved {len(ground_truth)} WebP images to {IMAGES_DIR}")
    print(f"Ground truth written to {GROUND_TRUTH_PATH}")
    print("\nQuery IDs for benchmarks.yaml:")
    for entry in ground_truth:
        print(f"  - {entry['id']}")


if __name__ == "__main__":
    main()
